"""Unit tests for ru_pulse.voice_lint.

Run:
    python -m pytest ru_pulse/tests/test_voice_lint.py -v

These tests require a populated `ru_pulse/reports/voice_fingerprint.json`,
generated by running `python -m ru_pulse.voice_fingerprint` against a
fetched corpus. In the public OSS distribution that fixture file is
intentionally absent (it bakes in private channel data); the tests
skip cleanly in that case. The exception-path test below explicitly
covers the missing-file case.
"""
