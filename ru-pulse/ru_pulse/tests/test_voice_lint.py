SHORT_TEXT = "ок"
EMPTY_TEXT = ""


def test_hype_text_high_hype_score():
    result = lint(HYPE_TEXT)
    assert result.hype_score > 0.6, (
        f"hype_score={result.hype_score} expected >0.6 for all-caps bullet post"
    )


def test_hype_text_returns_full_result():
    result = lint(HYPE_TEXT)
    assert isinstance(result, LintResult)
    assert len(result.nearest_buckets) == 3
    assert len(result.nearest_channels) == 5


def test_first_person_long_lands_personal_bucket():
    result = lint(FIRST_PERSON_LONG)
    top_bucket = result.nearest_buckets[0].bucket
    assert top_bucket in ("ai_core", "indie_solo", "prompt_specific"), (
        f"Expected personal bucket, got {top_bucket}"
    )


def test_first_person_low_broadcast():
    result = lint(FIRST_PERSON_LONG)
    assert result.broadcast_score < 0.7, (
        f"broadcast_score={result.broadcast_score} should be lower for first-person post"
    )


def test_empty_text_no_crash():
    result = lint(EMPTY_TEXT)
    assert isinstance(result, LintResult)
    assert result.verdict


def test_short_text_no_crash():
    result = lint(SHORT_TEXT)
    assert isinstance(result, LintResult)
    assert len(result.nearest_buckets) == 3


def test_short_json_serializable():
    result = lint(SHORT_TEXT)
    parsed = json.loads(result.to_json())
    assert "verdict" in parsed
    assert "nearest_buckets" in parsed


def test_broadcast_text_high_broadcast_score():
    result = lint(BROADCAST_TEXT)
    assert result.broadcast_score > 0.6, (
        f"broadcast_score={result.broadcast_score} expected >0.6 for impersonal news post"
    )


def test_listicle_text_listicle_score():
    result = lint(LISTICLE_TEXT)
    assert result.listicle_hits >= 1, (
        f"listicle_hits={result.listicle_hits} expected >=1 for numbered list post"
    )
    assert isinstance(result.listicle_hits, int)


def test_listicle_buckets_populated():
    result = lint(LISTICLE_TEXT)
    assert len(result.nearest_buckets) == 3
    for b in result.nearest_buckets:
        assert b.distance >= 0.0


def test_lint_with_empty_corpus_returns_unknown_verdict(monkeypatch):
    """Guard against IndexError when fingerprint corpus has no buckets."""
    from ru_pulse import voice_lint as vl
    empty_corpus = {"raw": [], "normalized": [], "bucket_centroids": {}}
    monkeypatch.setattr(vl, "_CORPUS", empty_corpus)
    result = vl.lint("any text")
    assert result.nearest_buckets == []
    assert result.nearest_channels == []
    assert "no buckets" in result.verdict.lower() or result.verdict


def test_load_corpus_missing_file_raises(monkeypatch, tmp_path):
    """Helpful error when fingerprint JSON not yet generated."""
    from ru_pulse import voice_lint as vl
    monkeypatch.setattr(vl, "_CORPUS", None)
    monkeypatch.setattr(vl, "_FP_PATH", tmp_path / "nonexistent.json")
    import pytest
    with pytest.raises(FileNotFoundError) as exc:
        vl._load_corpus()
    assert "voice_fingerprint" in str(exc.value)
